    public void llenarTabla() {
        for (int i = 0; i < tblProcesos.getRowCount(); i++) {
            modelo.removeRow(i);
            i -= 1;
        }
        
        ArrayList<Nodo> procesos = new ArrayList();
        procesos = l.imprimir();
        
        for (Nodo n : procesos) {
            modelo.addRow(new Object[]{
            n.getNombre(),
                n.getProcesos()
            });
        }
    }

